// Demonstrate the do-while loop.
class DoWhile {
    public static void main(String args[]) {
        int n = 10;
        do {
            System.out.println("tick " + n);
            n--;
        } while (n > 0);

        int m = 10;
        do {
            System.out.println("m " + m);
        } while (m > 11);
    }

    
}